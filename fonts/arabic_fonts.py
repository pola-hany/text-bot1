def font_arabic_bold(text):
    """خط عريض"""
    return f"𝐀𝐫𝐚𝐛𝐢𝐜: {text}"  # مؤقت، هنضيف التحويل الحقيقي

# ============= دوال تحويل الخطوط العربية =============

def arabic_convert(text, mapping):
    """تحويل النص العربي باستخدام خريطة"""
    result = ""
    for char in text:
        if char in mapping:
            result += mapping[char]
        else:
            result += char
    return result

